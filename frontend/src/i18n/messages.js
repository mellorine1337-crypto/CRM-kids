export const getMessage = (locale, key) => {
  const segments = key.split(".");
  let value = messages[locale];

  for (const segment of segments) {
    value = value?.[segment];
  }

  if (value === undefined && locale !== "ru") {
    return getMessage("ru", key);
  }

  return value;
};
